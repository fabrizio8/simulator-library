def test_even_0_or_1():
    traces = [
              ([(None, 4), ('1', 5), ('0', 5), ('0', 5), ('0', 5), ('1', 4)],True),
              ([(None, 4), ('1', 5), ('0', 5), ('1', 4), ('0', 4), ('1', 5)],True),
              ([(None, 4)], True),
              ([(None, 4)], True),
              ([(None, 2)],True),
              ([(None, 2)],True),
              ([(None, 2), ('1', 4)],False),
              ([(None, 2), ('1', 6)],False),
              ([(None, 4), ('1', 3)], False),
              ([(None, 2), ('0', 5)], False),
              ([(None, 2), ('1', 5), ('0', 5), ('0', 5), ('1', 4)], False),
              ([(None, 4), ('1', 5), ('0', 5), ('1', 5), ('0', 5), ('1', 5)],False),
            ]
    for trace in traces:
        assert even_0_or_1.oracle(*trace)

